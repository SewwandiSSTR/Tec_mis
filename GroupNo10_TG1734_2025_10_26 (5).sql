| Field  | Type          | Null | Key | Default | Extra |
+--------+---------------+------+-----+---------+-------+
| C_code | char(8)       | NO   | PRI | NULL    |       |
| C_type | enum('P','T') | NO   | PRI | NULL    |       |
| Regno  | char(12)      | NO   | PRI | NULL    |       |
+--------+---------------+------+-----+---------+-------+

